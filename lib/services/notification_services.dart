          importance: Importance.max,
        ),
      ),
      androidAllowWhileIdle: true,
      uiLocalNotificationDateInterpretation:
          UILocalNotificationDateInterpretation.absoluteTime,
      matchDateTimeComponents: DateTimeComponents.dateAndTime,
      payload: '${task.title}|${task.note}|${task.startTime}|',
    );
  }

  tz.TZDateTime _nextInstanceOfTenAM(int hour, int minutes, Task task) {
    final tz.TZDateTime now = tz.TZDateTime.now(tz.local);
    debugPrint('now $now');

    var formattedDate = DateFormat.yMMMMd().parse(task.date);

    final tz.TZDateTime formattedDateLocal =
        tz.TZDateTime.from(formattedDate, tz.local);

    tz.TZDateTime scheduledDate = tz.TZDateTime(
        tz.local,
        formattedDateLocal.year,
        formattedDateLocal.month,
        formattedDateLocal.day,
        hour,
        minutes);
    debugPrint('scheduledDate $scheduledDate');
    scheduledDate = timeAfterRemind(task.reminder, scheduledDate);

    if (scheduledDate.isBefore(now)) {
      if (task.repeat == 'Daily') {
        scheduledDate = tz.TZDateTime(tz.local, formattedDate.year,
            formattedDate.month, (formattedDate.day) + 1, hour, minutes);
      }
      if (task.repeat == 'Weekly') {
        scheduledDate = tz.TZDateTime(tz.local, formattedDate.year, now.month,
            (formattedDate.day) + 7, hour, minutes);
      }
      if (task.repeat == 'Monthly') {
        scheduledDate = tz.TZDateTime(tz.local, formattedDate.year,
            (formattedDate.month) + 1, formattedDate.day, hour, minutes);
      }
      scheduledDate = timeAfterRemind(task.reminder, scheduledDate);
    }

    debugPrint('next scheduledDate = $scheduledDate');
    return scheduledDate;
  }

  ///Function to substract remind from date
  tz.TZDateTime timeAfterRemind(int remind, tz.TZDateTime scheduledDate) {
    if (remind == 5) {
      scheduledDate = scheduledDate.subtract(const Duration(minutes: 5));
    }
    if (remind == 10) {
      scheduledDate = scheduledDate.subtract(const Duration(minutes: 10));
    }
    if (remind == 15) {
      scheduledDate = scheduledDate.subtract(const Duration(minutes: 15));
    }
    if (remind == 20) {
      scheduledDate = scheduledDate.subtract(const Duration(minutes: 20));
    }
    return scheduledDate;
  }

  void requestIOSPermissions() {
    flutterLocalNotificationsPlugin
